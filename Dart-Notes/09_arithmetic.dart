// Arithmetic operations in Dart

void main() {
  int a = 10, b = 3;
  double x = 5.5, y = 2.5;

  print('Addition (int): ${a + b}');
  print('Subtraction (int): ${a - b}');
  print('Multiplication (int): ${a * b}');
  print('Division (double): ${a / b}');
  print('Integer Division: ${a ~/ b}');
  print('Modulus: ${a % b}');

  print('Addition (double): ${x + y}');
  print('Multiplication (double): ${x * y}');
}

// Output:
// Addition (int): 13
// Subtraction (int): 7
// Multiplication (int): 30
// Division (double): 3.3333333333333335
// Integer Division: 3
// Modulus: 1
// Addition (double): 8.0
// Multiplication (double): 13.75
