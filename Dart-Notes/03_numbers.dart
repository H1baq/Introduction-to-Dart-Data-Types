// Numbers in Dart: int and double

void main() {
  int linesOfCodeWritten = 42;
  int bugsFixed = 10;

  double coffeeCups = 9.5;
  double codingHours = 3.14;

  int totalProductivity = linesOfCodeWritten + bugsFixed;
  double codeTime = coffeeCups * codingHours;

  print('Total productivity: $totalProductivity tasks');
  print('Coding time: $codeTime hours');
}

// Output:
// Total productivity: 52 tasks
// Coding time: 29.83 hours
