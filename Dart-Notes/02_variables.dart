// Variables in Dart
// Purpose: Learn different variable types and how to use them

void main() {
  String wizardName = 'Hibaq';  // Text
  int magicLevel = 100;           // Whole number
  double spellPower = 75.5;       // Decimal number
  bool hasMagicStaff = true;      // True/False

  print('Name: $wizardName');
  print('Magic Level: $magicLevel');
  print('Spell Power: $spellPower');
  print('Has Staff: $hasMagicStaff');

  // Updating variable
  magicLevel = 110;
  print('New Magic Level: $magicLevel');
}

// Output:
// Name: Hibaq
// Magic Level: 100
// Spell Power: 75.5
// Has Staff: true
// New Magic Level: 110
